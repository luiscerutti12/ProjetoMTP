    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (new Conexao().ConsultarExistente(jTextField1.getText())==true) {
            JOptionPane.showMessageDialog(null , "Usuário já existe, por favor escolha outro." , "Erro" , JOptionPane.ERROR_MESSAGE);
        }
        else {
            if (new String(jPasswordField1.getPassword()).equals(new String(jPasswordField2.getPassword()))==true) {
                //try {
                    Conexao novo = new Conexao ();
                    novo.inserir(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField4.getText(), new String(jPasswordField1.getPassword()), new String(jPasswordField2.getPassword()));
                //}
                //catch (SQLException e) {
                    //JOptionPane.showMessageDialog(null , e.getMessage());
                //}
                JOptionPane.showMessageDialog(null, "Usuário criado com sucesso.");
                new FazerLogin();
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(null, "As senhas se diferem, por favor corrija.");
                if ((new String(jPasswordField1.getPassword()))==null) {
                    JOptionPane.showMessageDialog(null, "A senha não pode ficar vazia.");
                }

            }

            if ((new String(jPasswordField1.getPassword()))=="") {
                JOptionPane.showMessageDialog(null, "A senha não pode ficar vazia.");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

